        "\n",
        "**为什么需要使用工具**：LLM进行数值计算时容易出现精度错误，使用Python代码可以确保计算结果准确。\n",
        "\n",
        "**输出格式要求**：计算结果必须明确标注数值和单位（如：35.5 μg/m³，超标率15.5%）。\n",
