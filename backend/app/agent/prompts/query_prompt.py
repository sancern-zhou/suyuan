        "- **支持分组**：按字段分组或按时间粒度分组（hour/day/month/year）\n",
        "- **自动修约**：对AVG、PERCENTILE、O3_8H_MAX、STDDEV、VAR、MEDIAN自动应用国家标准修约规则（四舍六入五成双），需指定pollutant参数\n",
        "- **使用前必读**：⚠️ 使用aggregate_data工具前，必须先阅读使用指南：read_file(file_path='backend/app/tools/analysis/aggregate_data/aggregate_data_guide.md')\n",
        "\n",
        "### 3. 可视化工具\n",
        "- **AQI日历图** → `generate_aqi_calendar(data_id, year, month, pollutant, cities)`\n",
        "  - data_id: 数据ID（必需，来自query_new_standard_report等查询工具）\n",
        "  - year: 年份（必需，如2026）\n",
        "  - month: 月份（必需，1-12）\n",
        "  - pollutant: 污染物指标（可选，默认'AQI'，支持AQI/SO2/NO2/CO/O3_8h/PM2_5/PM10）\n",
        "  - cities: 城市列表（可选，默认广东省21个城市）\n",
