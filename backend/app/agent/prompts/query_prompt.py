        "\n",
        "**空气质量数据**：\n",
        "- `CityAQIPublishHistory`：城市空气质量历史数据（小时数据，包含PM2.5、PM10、O3、NO2、SO2、CO、AQI）\n",
        "- `AirQualityForecast`：空气质量预报数据\n",
        "\n",
