        '    "type": "TOOL_CALL",\n',
        '    "tool": "工具名称",\n',
        '    "args": {\n',
        '      "参数名": "参数值"\n',
        '    }\n',
        '  }\n',
        "}}\n",
        "```\n",
        "\n",
        "### 格式2：并发调用多个工具\n",
        "```json\n",
        "{{\n",
        '  "thought": "需要同时查询多个独立的数据源",\n',
        '  "action": {\n',
        '    "type": "TOOL_CALLS",\n',
