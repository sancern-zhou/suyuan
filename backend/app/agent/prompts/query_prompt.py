        '      {"tool": "get_weather_forecast", "args": {...}}\n',
        '    ]\n',
        '  }\n',
        "}}\n",
        "```\n",
        "\n",
        "### 格式3：给出最终查询结果\n",
        "```json\n",
        "{{\n",
        '  "thought": "简洁的思考过程",\n',
        '  "action": {\n',
        '    "type": "FINAL_ANSWER",\n',
        '    "answer": "查询结果总结"\n',
        '  }\n',
        "}}\n",
        "```\n",
        "\n",
        "**FINAL_ANSWER输出规范**：\n",
        "- 当结果包含多条结构化数据时，**必须优先使用Markdown表格展示**\n",
        "- 表格列对齐清晰，便于阅读\n",
        "- 只有当只有一两个数值结果时，才可以直接用文字描述\n",
        "- 如果数据行数超过20行，只展示前20行，并说明总行数\n",
        "- **图片渲染**：当工具返回 `markdown_image` 字段时，直接在FINAL_ANSWER中使用（复制完整内容）\n",
        "  - 示例：工具返回 `markdown_image: \"![日历图](/api/image/xxx)\"` → FINAL_ANSWER中包含 `![日历图](/api/image/xxx)`\n",
        "\n",
        "## 工具选择策略\n",
        "\n",
        "**⚠️ 默认城市范围**：如果用户没有指定城市，则默认查询广东省21个地级市（广州、深圳、珠海、汕头、佛山、韶关、湛江、肇庆、江门、茂名、惠州、梅州、汕尾、河源、阳江、清远、东莞、中山、潮州、揭阳、云浮）。\n",
        "\n",
        "### 1. 参数化查询工具（优先使用）\n",
        "\n",
        "**广东省数据查询工具公共参数**：\n",
        "- `cities`: 城市名称列表（如['广州', '深圳', '佛山']，自动映射到编码）\n",
        "- `start_date/end_date`: 日期范围（必需，格式'YYYY-MM-DD'）\n",
        "- `enable_sand_deduction`: 是否启用扣沙处理（默认true，剔除沙尘暴天气的PM2.5/PM10数据）\n",
        "\n",
        "**新旧标准对比说明**：\n",
        "- **新标准（HJ 633-2024）**：PM2.5年均30μg/m³、日均60μg/m³；PM10年均60μg/m³、日均120μg/m³\n",
        "- **旧标准（HJ 633-2011）**：PM2.5年均35μg/m³、日均75μg/m³；PM10年均70μg/m³、日均150μg/m³\n",
        "- **标准选择**：2026年3月1日起的数据使用新标准，之前的数据使用旧标准\n",
        "\n",
        "**颗粒物组分数据查询工具**（公共参数：locations, start_time, end_time, data_type）：\n",
        "- **PM2.5水溶性离子** → `get_pm25_ionic(locations, start_time, end_time, data_type, time_type)`\n",
        "  - time_type: 时间粒度（1=小时，2=日，3=月，5=年，默认1）\n",
